
export class FQuaternion
{
    public x: number;
    public y: number;
    public z: number;
    public w: number;


    //////////////////////////////////////////////////////////////////////////
    constructor( x: number = 0, y: number = 0, z: number = 0, w: number = 1 )
    {
        this.x = x;
        this.y = y;
        this.z = z;
        this.w = w;
    };
};
